// ============================================================================
//  Program.cs — ТОЧКА ВХОДА всего приложения.
//
//  Когда ты запускаешь "dotnet run", выполнение начинается ОТСЮДА, сверху вниз.
//  Здесь нет class Program и нет метода Main — это "top-level statements"
//  (фича C#: компилятор сам заворачивает этот файл в Main за тебя).
//
//  Файл делится на ДВЕ ФАЗЫ. Запомни это деление — оно одинаковое во всех
//  ASP.NET Core проектах, включая большую Favilonia:
//
//    ФАЗА 1 (НАСТРОЙКА):   что ПРИЛОЖЕНИЕ УМЕЕТ         — строки с builder.Services...
//    ФАЗА 2 (КОНВЕЙЕР):    как ОБРАБАТЫВАЕТСЯ ЗАПРОС     — строки с app.Use... / app.Map...
//
//  Граница между фазами — строка "var app = builder.Build();".
//  Подробный разбор каждой строки — в README.md этой папки.
// ============================================================================


// --- ФАЗА 1: НАСТРОЙКА (регистрируем то, что приложению понадобится) ---------

// builder — это "конструктор приложения". Внутри у него лежит:
//   - builder.Services       — контейнер зависимостей (DI). Сюда мы складываем сервисы.
//   - builder.Configuration  — конфигурация (appsettings.json, переменные окружения).
var builder = WebApplication.CreateBuilder(args);

// Регистрируем поддержку КОНТРОЛЛЕРОВ.
// Без этой строки ASP.NET не будет искать классы-контроллеры и их методы.
// В Favilonia ровно то же самое делается через builder.Services.AddApiInfrastructure()
// (там просто завёрнуто в extension-метод, но внутри — этот же AddControllers).
builder.Services.AddControllers();

// Build() "запечатывает" настройку и собирает готовое приложение.
// После этой строки добавлять сервисы в builder.Services уже НЕЛЬЗЯ.
var app = builder.Build();


// --- ФАЗА 2: КОНВЕЙЕР ОБРАБОТКИ ЗАПРОСА (middleware pipeline) -----------------

// Каждый HTTP-запрос проходит сверху вниз через этот список "прослоек" (middleware).
// Порядок строк = порядок прохождения. Это важно: поменяешь местами — изменишь поведение.

// MapControllers() ставит в конце конвейера "роутер": он смотрит на URL запроса
// (например GET /api/ping) и находит подходящий метод в подходящем контроллере.
app.MapControllers();

// Run() запускает веб-сервер (Kestrel) и БЛОКИРУЕТ поток — приложение начинает
// слушать входящие запросы и крутится здесь, пока ты не остановишь его (Ctrl+C).
app.Run();
